package d.operators;

public class Relational {
    public static void main(String[] args) {
        int a=45;
        int b=39;

        System.out.println(a==b); //false
        System.out.println(a!=b); //false true
        System.out.println(a>b); //true
        System.out.println(a<b); //false
        System.out.println(a>=b);//true
        System.out.println(a<=b);//false

    }
}
